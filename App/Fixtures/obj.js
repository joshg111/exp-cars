class Node {
  constructor(data) {
    this.data = data;
    this.children = {};
    this.word = '';

  }
};

function create_prefix_tree(d) {
  var head = new Node('');
  var curr = head;
  for (var word of Object.keys(d)) {
    curr = head;

    for (var char of word.split('')) {

      if (!(char.toLowerCase() in curr.children)) {

        var n = new Node(char);
        n.word = curr.word + char;
        curr.children[char.toLowerCase()] = n;
      }

      curr = curr.children[char.toLowerCase()];
    }
  }
  return head;
};

function find_words(tree, d, prefix) {
  var curr = tree;
  var res = [];
  prefix = prefix.toLowerCase();
  for (char of prefix.split('')) {
    if (!(char in curr.children)) {

      return [];
    }
    curr = curr.children[char];
  }

  var stack = [];
  stack.push(curr);
  while (stack.length > 0) {
    curr = stack.pop();
    if (curr.word in d) {

      res.push(curr.word);
    }
    for (node of Object.keys(curr.children).map(function(key) {return curr.children[key]})) {
      stack.push(node);

    }
  }

  return res;
};

class MakeLookup {
  constructor() {
    this.tree = create_prefix_tree(car_data);
  }

  find_words(prefix) {
    return find_words(this.tree, car_data, prefix).reverse()
  }
}

class ModelLookup {
  constructor(make) {
    this.make = make;
    var res = {};
    for (var i of car_data[make]) {
      res[i] = 1;
    }
    this.dict = res;
    this.tree = create_prefix_tree(this.dict);
  }

  find_words(prefix) {
    return find_words(this.tree, this.dict, prefix).reverse()
  }
}

